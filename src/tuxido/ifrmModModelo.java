    public void guardar(){
        conexion();
                if(cmodelo.getText().length()>0&&
                desc.getText().length()>0&&
                anio.getText().length()>0&&
                pref.getText().length()>0&&
                serieprod.getText().length()>0&&
                serieemp.getText().length()>0){
            
          if(anio.getText().length()==4){
              if(pref.getText().length()==8){
                  if(serieprod.getText().length()==6){
                        cnxO.ejecutar("update TX_MODELO SET codigo_modelo='"+cmodelo.getText()+"' WHERE id='"+ind+"'");
                        cnxO.ejecutar("update TX_MODELO SET descripcion='"+desc.getText()+"' WHERE id='"+ind+"'");
                        cnxO.ejecutar("update TX_MODELO SET anio='"+anio.getText()+"' WHERE id='"+ind+"'");
                        cnxO.ejecutar("update TX_MODELO SET prefijo='"+pref.getText()+"' WHERE id='"+ind+"'");
                        cnxO.ejecutar("update TX_MODELO SET ini_serie_prod='"+serieprod.getText()+"' WHERE id='"+ind+"'");
                        cnxO.ejecutar("update TX_MODELO SET ini_serie_emp='"+serieemp.getText()+"' WHERE id='"+ind+"'");
                        JOptionPane.showMessageDialog(null,"El Registro fue modificado con exito");
                        dispose();
                  }else{
                      JOptionPane.showMessageDialog(null,"El campo de la Serie para produccion debe contener 6 digitos");
                  }
              }else{
                  JOptionPane.showMessageDialog(null,"El campo del Prefijo debe contener 8 digitos");
              }
          }else{
              JOptionPane.showMessageDialog(null,"El campo del Año debe contener 4 digitos ejem. ''2013''");
          } 
        }else{
          JOptionPane.showMessageDialog(null,"Existen Campos vacios, valida la informacion");
        }
        desconexion();
    }
