    public void informacion(){
        conexion();
        cnxO.consulta("select * from TX_MODELO WHERE id="+ind+"");
        try {
            while(cnxO.reg.next()){
                cmodelo.setText(cnxO.reg.getString(1));
                desc.setText(cnxO.reg.getString(2));
                anio.setText(cnxO.reg.getString(3));
                pref.setText(cnxO.reg.getString(4));
                serieprod.setText(cnxO.reg.getString(6));
                serieemp.setText(cnxO.reg.getString(7));
            }
            cnxO.reg.close();
        } catch (SQLException ex) {
            Logger.getLogger(ifrmModModelo.class.getName()).log(Level.SEVERE, null, ex);
        }
        desconexion();
    }
