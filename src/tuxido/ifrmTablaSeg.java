        // TODO add your handling code here:
                Date fechaActual=new Date();
                SimpleDateFormat forFecha=new SimpleDateFormat("MM-dd-yyyy");
                fecha=forFecha.format(fechaActual);
        if(real.isSelected()==true){
            query="select ID, VIN, MOD_DESC, COL_DESC, ANIO, "
                    + "WE_STATUS, TO_CHAR(WE_TIM,'DD/MM/YYYY HH:MI:SS AM'), "
                    + "PA_STATUS, TO_CHAR(PA_TIM,'DD/MM/YYYY HH:MI:SS AM'), "
                    + "AF_STATUS, TO_CHAR(AF_TIM,'DD/MM/YYYY HH:MI:SS AM'), "
                    + "VQ_STATUS, TO_CHAR(VQ_TIM,'DD/MM/YYYY HH:MI:SS AM'), "
                    + "PK_STATUS, TO_CHAR(PK_TIM,'DD/MM/YYYY HH:MI:SS AM'), "
                    + "SHP_STATUS, TO_CHAR(SHP_TIM,'DD/MM/YYYY HH:MI:SS AM') "
                    + "from TX_ORDEN_PROD WHERE SHP_STATUS='Pendiente' order by ID ASC";
        }else{
            query="select ID, VIN, MOD_DESC, COL_DESC, ANIO, "
                    + "WE_STATUS, TO_CHAR(WE_TIM,'DD/MM/YYYY HH:MI:SS AM'), "
                    + "PA_STATUS, TO_CHAR(PA_TIM,'DD/MM/YYYY HH:MI:SS AM'), "
                    + "AF_STATUS, TO_CHAR(AF_TIM,'DD/MM/YYYY HH:MI:SS AM'), "
                    + "VQ_STATUS, TO_CHAR(VQ_TIM,'DD/MM/YYYY HH:MI:SS AM'), "
                    + "PK_STATUS, TO_CHAR(PK_TIM,'DD/MM/YYYY HH:MI:SS AM'), "
                    + "SHP_STATUS, TO_CHAR(SHP_TIM,'dd/MM/yyy HH:mm:ss') "
                    + " from TX_ORDEN_PROD WHERE ID_ORDEN='"+orden.getText()+"' order by ID ASC";
        }
        hilo=new hiloTablaSeg(tabla,query);
        INICIAR.setEnabled(false);
        DETENER.setEnabled(true);
        hilo.start();
